package com.nikpappas.processing.math;

public class Util {
    public static float sq(float a){
        return a*a;
    }
    public static int sq(int a){
        return a*a;
    }
    public static long sq(long a){
        return a*a;
    }
    public static double sq(double a){
        return a*a;
    }
}
